Leagues = new Mongo.Collection('leagues');

Leagues.attachSchema(new SimpleSchema({
  name: { type: String }
}));

if (Meteor.isServer) {
  Leagues.allow({
    insert: function (userId, doc) {
      return false;
    },

    update: function (userId, doc, fieldNames, modifier) {
      return false;
    },

    remove: function (userId, doc) {
      return false;
    }
  });

  Leagues.deny({
    insert: function (userId, doc) {
      return true;
    },

    update: function (userId, doc, fieldNames, modifier) {
      return true;
    },

    remove: function (userId, doc) {
      return true;
    }
  });
}
